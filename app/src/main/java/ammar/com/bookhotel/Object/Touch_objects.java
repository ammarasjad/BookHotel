package ammar.com.bookhotel.Object;


public class Touch_objects {
    private int x = 0 , y = 0;

    public Touch_objects(int x, int y) {
        setY(y);
        setX(x);
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
